package com.orka.publicsampletransport;

public class Staff {

     String sname;
     int sno;


    public Staff(){

    }

    public Staff(String sname,int sno){
        this.sname=sname;
        this.sno=sno;
    }

    public String getSname(){
        return sname;
    }

    public int getSno(){
        return sno;
    }


}
